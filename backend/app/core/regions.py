def get_region_code(sido: str, sigungu: str) -> str | None:
    """시/도와 시/군/구 이름으로 지역 코드 반환"""
    sido_regions = REGION_CODES.get(sido, {})
    return sido_regions.get(sigungu)


def get_all_sido() -> list[str]:
    """전체 시/도 목록 반환"""
    return list(SIDO_CODES.keys())


def get_sigungu_list(sido: str) -> list[str]:
    """해당 시/도의 시/군/구 목록 반환"""
    return list(REGION_CODES.get(sido, {}).keys())


def get_flat_region_list() -> list[dict]:
    """전체 지역을 플랫 리스트로 반환 (API 응답용)"""
    result = []
    for sido, districts in REGION_CODES.items():
        for sigungu, code in districts.items():
            result.append({
                "sido": sido,
                "sigungu": sigungu,
                "code": code,
            })
    return result
